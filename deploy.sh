log_info "–î–ª—è –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞: pm2 restart websocket-chat"
log_info "–î–ª—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏: pm2 stop websocket-chat" 